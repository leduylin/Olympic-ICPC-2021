package cses_5000;

import java.io.*;
import java.util.*;

public class CreatingStrings {

	public static void main(String[] args) {
		String stringGive = ns();
		List<String> res = new ArrayList<>();
		HashMap<String, Integer> stringMap = new HashMap<>();
		permute(stringGive, 0, stringGive.length() - 1, res, stringMap);
		StringBuilder outBuffer = new StringBuilder();
		outBuffer.append(res.size() + "\n");
		res.sort((a1, a2) -> a1.compareTo(a2));
		for (String a : res) {
			outBuffer.append(a + "\n");
		}
		System.out.println(outBuffer);

	}

	static void permute(String stringGiven, int left, int right, List<String> res, HashMap<String, Integer> stringMap) {
		if (left == right) {
			if (stringMap.get(stringGiven) == null) {
				res.add(stringGiven);
				stringMap.put(stringGiven, 1);
			}
		} else {
			for (int i = left; i <= right; i++) {
				stringGiven = swap(stringGiven, left, i);
				permute(stringGiven, left + 1, right, res, stringMap);
				stringGiven = swap(stringGiven, left, i);
			}
		}
	}

	static String swap(String a, int i, int j) {
		char temp;
		char[] charArray = a.toCharArray();
		temp = charArray[i];
		charArray[i] = charArray[j];
		charArray[j] = temp;
		return String.valueOf(charArray);
	}

	static InputStream is = System.in;
	static byte[] inbuf = new byte[1 << 24];
	static int lenbuf = 0, ptrbuf = 0;

	static int readByte() {
		if (lenbuf == -1)
			throw new InputMismatchException();
		if (ptrbuf >= lenbuf) {
			ptrbuf = 0;
			try {
				lenbuf = is.read(inbuf);
			} catch (IOException e) {
				throw new InputMismatchException();
			}
			if (lenbuf <= 0)
				return -1;
		}
		return inbuf[ptrbuf++];
	}

	static boolean isSpaceChar(int c) {
		return !(c >= 33 && c <= 126);
	}

	static int skip() {
		int b;
		while ((b = readByte()) != -1 && isSpaceChar(b))
			;
		return b;
	}

	static double nd() {
		return Double.parseDouble(ns());
	}

	static char nc() {
		return (char) skip();
	}

	static String ns() {
		int b = skip();
		StringBuilder sb = new StringBuilder();
		while (!(isSpaceChar(b))) {
			sb.appendCodePoint(b);
			b = readByte();
		}
		return sb.toString();
	}

	static char[] ns(int n) {
		char[] buf = new char[n];
		int b = skip(), p = 0;
		while (p < n && !(isSpaceChar(b))) {
			buf[p++] = (char) b;
			b = readByte();
		}
		return n == p ? buf : Arrays.copyOf(buf, p);
	}

	static int ni() {
		int num = 0, b;
		boolean minus = false;
		while ((b = readByte()) != -1 && !((b >= '0' && b <= '9') || b == '-'))
			;
		if (b == '-') {
			minus = true;
			b = readByte();
		}
		while (true) {
			if (b >= '0' && b <= '9') {
				num = num * 10 + (b - '0');
			} else {
				return minus ? -num : num;
			}
			b = readByte();
		}
	}

	static long nl() {
		long num = 0;
		int b;
		boolean minus = false;
		while ((b = readByte()) != -1 && !((b >= '0' && b <= '9') || b == '-'))
			;
		if (b == '-') {
			minus = true;
			b = readByte();
		}
		while (true) {
			if (b >= '0' && b <= '9') {
				num = num * 10 + (b - '0');
			} else {
				return minus ? -num : num;
			}
			b = readByte();
		}
	}

}
